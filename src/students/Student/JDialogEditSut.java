    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        if (checkStData() == true) {
            int insetStudent = new StudenDB().insetStudent(jTextFieldName.getText().trim(), jTextFieldCode.getText().trim(), jTextFieldAddress.getText().trim(), jTextFieldPhone.getText().trim(), jTextArea1.getText());
            if (insetStudent > 0) {
                JOptionPane.showMessageDialog(this, "تم اضافة الطالب بنجاح");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "حدث خطأ اثناء عملية الاضافة");

            }
        }

    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        if (checkStData() == true) {
            int UpdateStudent = new StudenDB().UpdateStudent(getStudent().getId(), jTextFieldName.getText().trim(), jTextFieldCode.getText().trim(), jTextFieldAddress.getText().trim(), jTextFieldPhone.getText().trim(), jTextArea1.getText());
            if (UpdateStudent > 0) {
                JOptionPane.showMessageDialog(this, "تم تحديث البيانات بنجاح");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "حدث خطأ اثناء تحديث البيانات");
            }
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDelActionPerformed
        int delStudent = new StudenDB().delStudent(getStudent().getId());
        if(delStudent > 0){
        JOptionPane.showMessageDialog(this, "تم حذف الطالب بنجاح");
        this.dispose();
        }else{
        JOptionPane.showMessageDialog(this, "حدث خطأ اثناء عملية الحذف");
        }
    }//GEN-LAST:event_jButtonDelActionPerformed

