    private boolean checkStData() {
        if (jTextFieldName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "الرجاء ادخال اسم الطالب");
        } else if (jTextFieldCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "الرجاء ادخال كود صحيح");
        } else if (jTextFieldPhone.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "الرجاء ادخال رقم الهاتف");
        } else if (jTextFieldAddress.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "الرجاء ادخال عنوان الطالب");
        } else {
            return true;
        }
        return false;
    }

    private void setStudentData(Student student) {
        jLabelID.setText("" + student.getId());
        jTextFieldName.setText(student.getName());
        jTextFieldAddress.setText(student.getAdress());
        jTextFieldCode.setText(student.getCode());
        jTextFieldPhone.setText(student.getPhone());
        jTextArea1.setText(student.getNots());

    }

